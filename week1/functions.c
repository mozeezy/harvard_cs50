#include <stdio.h>

// functions are block(s) of code that takes in an input (or argument) and returns an output. it is commonly used by programmers to allow for reusability of code.

say_hello(name)
{
  return printf("Hello, ", name);
}

// In the function above, the function's name is "say_hello" with the name as an input. When this function is called, it runs the print statement and RETURNS the value of that statement as an output that we can store and use later.
